module sipo8bit (
    input  logic       clk,
    input  logic       rstn,
    input  logic       serial_in,
    output logic [7:0] parallel_out
);

    logic [7:0] q_stage;

    //============================================================
    // Flip-Flop 0
    //============================================================
    flipflopD FF0_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (serial_in),
        .q    (q_stage[0])
    );

    //============================================================
    // Flip-Flop 1
    //============================================================
    flipflopD FF1_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[0]),
        .q    (q_stage[1])
    );

    //============================================================
    // Flip-Flop 2
    //============================================================
    flipflopD FF2_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[1]),
        .q    (q_stage[2])
    );

    //============================================================
    // Flip-Flop 3
    //============================================================
    flipflopD FF3_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[2]),
        .q    (q_stage[3])
    );

    //============================================================
    // Flip-Flop 4
    //============================================================
    flipflopD FF4_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[3]),
        .q    (q_stage[4])
    );

    //============================================================
    // Flip-Flop 5
    //============================================================
    flipflopD FF5_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[4]),
        .q    (q_stage[5])
    );

    //============================================================
    // Flip-Flop 6
    //============================================================
    flipflopD FF6_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[5]),
        .q    (q_stage[6])
    );

    //============================================================
    // Flip-Flop 7
    //============================================================
    flipflopD FF7_inst (
        .clk  (clk),
        .rstn (rstn),
        .d    (q_stage[6]),
        .q    (q_stage[7])
    );

    assign parallel_out = q_stage;

endmodule
